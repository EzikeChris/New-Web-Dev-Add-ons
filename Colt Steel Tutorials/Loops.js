/// FOR LOOPS IN JAVASCRIPT ////
for (let i = 1; i < 5; i++) {
  // intialExpression let i =1
  // Condtion i <= 5
  // IncrementExpression i++
  console.log(i);
}

for (let i = 0; i <= 20; i += 2) {
  console.log(i);
}
// PRINTS OUT EVEN NUMBERS FROM 2

for (let i = 100; i >= 0; i -= 10) {
  console.log(i);
}
// prints out numbers decrementally from  100

// INFINITE LOOPS IN JAVASCRIPT//

for (let i = 20; i >= 0; i++) {
  console.log(i);
}

